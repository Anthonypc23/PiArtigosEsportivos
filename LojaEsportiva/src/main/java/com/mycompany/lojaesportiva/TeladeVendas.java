    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String Marcas = Marcasgrupo1.getSelection().getActionCommand();
        
        String Tamanho = Tamanhogrupo2.getSelection().getActionCommand();
        
        String Produto = Produtosgrupo3.getSelection().getActionCommand();
        
        String Servicos = "";
        if(chkGarantia.isSelected()){
            Servicos += chkGarantia.getText() + " ";
        }
        if(chkDesconto.isSelected()){
            Servicos += chkDesconto.getText() + " ";
        }
        if(chkParcelamentos.isSelected()){
            Servicos += chkParcelamentos.getText() + " ";
        }
        
        String Parcelamentos = cboTipoParce.getSelectedItem().toString();

        
        JOptionPane.showMessageDialog(this, "Vocรช Selecionou a marca: " + Marcas + "\n" +
                "Tamanho: " + Tamanho + "\n" +
                "Produto: " + Produto + "\n" +
                "Servicos: " + Servicos + "\n" +
                "Parcelamento: " + Parcelamentos 
        );
    }//GEN-LAST:event_btnSalvarActionPerformed

